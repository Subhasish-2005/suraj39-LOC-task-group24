#include<iostream>
using namespace std;
int main(){
    float r;
    cout<<"enter radius of circle: ";
    cin>>r;
    cout<<"the diameter of the circle is: ";
    cout<<2*r<<endl;
    cout<<"the circumference of the circle is: ";
    cout<<2*3.142*r<<endl;
    cout<<"the area of the circle is: ";
    cout<<3.142*r*r<<endl;
    return 0;
}
